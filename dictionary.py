#dictionaries use curly brackets
#unlike lists, dictionaries store info in pairs or multiple pairs

good_boi = {"name": "Rex", 
            "animal": "dog", 
            "no_hobbies": 4, 
            "wakeup_weekend": 10}

print(f'Hello I am {good_boi["name"]} and I am a {good_boi["animal"]}.')

print(f'I have  {good_boi["no_hobbies"]}.')

print(f'On the weekend I get up at  {good_boi["wakeup_weekend"]}.')

